def fetch(url):
    req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0 classics40-builder"})
    with urllib.request.urlopen(req, timeout=20) as r:
        return r.read().decode("utf-8", errors="replace")


def clean(s):
    s = re.sub(r"<script[\s\S]*?</script>", "", s)
    s = re.sub(r"<style[\s\S]*?</style>", "", s)
    s = re.sub(r"<[^>]+>", "", s)
    s = html.unescape(s)
    s = s.replace("\r", "\n")
    s = re.sub(r"[ \t\xa0]+", " ", s)
    s = re.sub(r"\n\s*\n\s*\n+", "\n\n", s)
    return s.strip()


def links():
    out = []
    seen = set()
    for url in INDEXES:
        page = fetch(url)
        for m in re.finditer(r'href="(https://cerclearning\.tp\.edu\.tw/classical/datapage/\d+)">([^<]+)</a>', page):
            if m.group(1) not in seen:
                seen.add(m.group(1))
                out.append((m.group(1), clean(m.group(2))))
    return out[:40]


def parse_detail(url, fallback_title):
    page = fetch(url)
    text = clean(page)
    title = fallback_title
    original = ""

    paras = []
    for m in re.finditer(r"．原文第\d+段\s*([\s\S]*?)\s*．段落語譯", text):
        p = clean(m.group(1))
        if p and p not in paras:
            paras.append(p)
    if paras:
        original = "\n\n".join(paras)
    else:
        m = re.search(r"原文\s*([\s\S]*?)\s*語譯", text)
        if m:
            original = clean(m.group(1))

    return title, original


